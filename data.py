def rename_node(n):
    if n == "ad2_ad3_pt": 
        return "Entrance_Main"
    if not n.startswith("S"): # If it doesn't start with S (like S6, S7), it's a road
        return f"Inters_{n}"
    return n

def get_graph_data():
    # Automatically rename everything before sending it to the app!
    fixed_pos = {rename_node(k): v for k, v in nodes_pos.items()}
    fixed_info = {rename_node(k): v for k, v in nodes_info.items()}
    fixed_edges = [[rename_node(u), rename_node(v), w] for u, v, w in raw_edges]
    
    return fixed_pos, fixed_edges, fixed_info
